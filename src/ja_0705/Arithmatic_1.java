package ja_0705;

class Arithmatic_1 
{
	public static void main(String[] args) 
	{
		
		float a, b, c, d, e, g;
		float f;

		a=10;
		b=3;

		c = a + b;
		d = a - b;
		e = a * b;
		f = a / b;
		g = a % b; //나머지 연산자

		System.out.println("a + b = "+c);
		System.out.println("a - b = "+d);	
		System.out.println("a * b = "+e);	
		System.out.println("a / b = "+f);	
		System.out.println("a % b = "+g);	

		
		System.out.println("Hello World!");
	}
}
