package ja_0704;

class DataType_1
{
	public static void main(String[] args) 
	{
		
	//변수 초기화

	byte bb = 10;
	short ss = 077;
	int ii = 0xff;
	long ll = 10L;
		
	System.out.println("byte =>" + bb);
	System.out.println("short =>" + ss);
	System.out.println("int =>" + ii);
	System.out.println("long =>" + ll);
	System.out.println("Hello World!");
	}
}
